CREATE TABLE IF NOT EXISTS public.employee_profiles
(
    id_employee_profile bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    id_user             bigint                                NOT NULL,
    employee_code       character varying(30),
    first_name          character varying(80),
    last_name           character varying(120),
    corporate_email     character varying(120),
    phone               character varying(30),
    identity_document   character varying(40),
    birth_date          date,
    address             character varying(160),
    city                character varying(80),
    state_province      character varying(80),
    postal_code         character varying(20),
    country             character varying(80),
    job_title           character varying(100),
    department          character varying(100),
    hire_date           date,
    status              character varying(20)                 NOT NULL DEFAULT 'ACTIVE',
    contract_type       character varying(30),
    internal_notes      character varying(1000),
    CONSTRAINT employee_profiles_pkey PRIMARY KEY (id_employee_profile),
    CONSTRAINT uk_employee_profiles_user UNIQUE (id_user),
    CONSTRAINT uk_employee_profiles_employee_code UNIQUE (employee_code),
    CONSTRAINT fk_employee_profiles_user FOREIGN KEY (id_user) REFERENCES public.users (id_user)
);

